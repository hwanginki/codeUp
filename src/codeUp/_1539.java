package codeUp;

import java.util.*;

public class _1539 {
	
	static void f(int n) {
		if (0 == n) {
			 System.out.print("false");
		} else {
			System.out.print("true");
		}
	}
	
	public static void main(String[] args) {
		
		Scanner sc = new Scanner(System.in);
		int n = sc.nextInt();

		f(n);
		sc.close();
		
	}
	
}
